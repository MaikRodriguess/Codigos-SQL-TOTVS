 SELECT 
	SPLETIVO.CODPERLET,
	GFILIAL.NOME,
	SMATRICULA.RA,
	PPESSOA.NOME,
	STURMADISC.IDTURMADISC,
	STURMADISC.CODTURMA,
	SDISCIPLINA.CODDISC,
	SDISCIPLINA.NOME,
	SSTATUS.DESCRICAO STATUS_DISC
FROM SMATRICULA
	INNER JOIN PPESSOA (NOLOCK)
		ON SMATRICULA.RA = PPESSOA.CODIGO
	INNER JOIN STURMADISC (NOLOCK)
		ON SMATRICULA.IDTURMADISC = STURMADISC.IDTURMADISC AND SMATRICULA.IDPERLET = STURMADISC.IDPERLET
	INNER JOIN SPLETIVO (NOLOCK)
		ON STURMADISC.CODCOLIGADA = SPLETIVO.CODCOLIGADA AND STURMADISC.IDPERLET = SPLETIVO.IDPERLET
	INNER JOIN GFILIAL (NOLOCK)
		ON STURMADISC.CODCOLIGADA = GFILIAL.CODCOLIGADA AND STURMADISC.CODFILIAL = GFILIAL.CODFILIAL 
	INNER JOIN SDISCIPLINA (NOLOCK)
		ON STURMADISC.CODDISC = SDISCIPLINA.CODDISC AND STURMADISC.CODCOLIGADA = SDISCIPLINA.CODCOLIGADA
	INNER JOIN SSTATUS (NOLOCK)
		ON SMATRICULA.CODCOLIGADA = SSTATUS.CODCOLIGADA AND SMATRICULA.CODSTATUS = SSTATUS.CODSTATUS 

	/*INNER JOIN SHABILITACAOALUNO (NOLOCK)
		ON STURMADISC.CODCOLIGADA = SHABILITACAOALUNO.CODCOLIGADA AND STURMADISC.IDHABILITACAOFILIAL = SHABILITACAOALUNO.IDHABILITACAOFILIAL*/
